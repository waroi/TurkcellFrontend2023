class Music {
  constructor(id, imageUrl, name, category, date, writer, textContent, clock) {
    this.id = id;
    this.imageUrl = imageUrl;
    this.name = name;
    this.category = category;
    this.date = date;
    this.writer = writer;
    this.textContent = textContent;
    this.clock = clock;
  }
}

export default Music;
