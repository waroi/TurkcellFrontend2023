class Product {
  constructor(id, imageUrl, name, category, brand, price, oldPrice, stock) {
    this.id = id;
    this.imageUrl = imageUrl;
    this.name = name;
    this.category = category;
    this.brand = brand;
    this.price = price;
    this.oldPrice = oldPrice;
    this.stock = stock;
  }
}

export default Product;
