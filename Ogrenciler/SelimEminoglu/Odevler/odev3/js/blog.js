class blog {
  constructor(
    id,
    title,
    body,
    author,
    date,
    hour,
    category,
    url,
    authorPicture
  ) {
    this.id = id;
    this.title = title;
    this.body = body;
    this.author = author;
    this.date = date;
    this.hour = hour;
    this.category = category;
    this.url = url;
    this.authorPicture = authorPicture;
  }
}
